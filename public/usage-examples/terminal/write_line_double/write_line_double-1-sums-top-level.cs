using static SplashKitSDK.SplashKit;

WriteLine("Demonstrating WriteLine(double):");

// Perform some calculations
double value1 = 3.14159;  // Example value: pi
double value2 = 2.71828;  // Example value: e
double sum = value1 + value2;
double product = value1 * value2;
double difference = value1 - value2;
double quotient = value1 / value2;

// Output the results using WriteLine(double data)
WriteLine("The values used are:");
WriteLine(value1); // Writing the first double value
WriteLine(value2); // Writing the second double value

WriteLine("Their sum is:");
WriteLine(sum); // Writing the sum

WriteLine("Their product is:");
WriteLine(product); // Writing the product

WriteLine("Their difference is:");
WriteLine(difference); // Writing the difference

WriteLine("Their quotient is:");
WriteLine(quotient); // Writing the quotient

WriteLine("End of demonstration.");